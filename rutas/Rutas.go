package rutas

import (
	"Proyecto/funciones"

	"github.com/gofiber/fiber/v3"
)

func Rutas(app *fiber.App) {

	/////////////////////////////////////////////
	//                RUTAS:'GET'              //
	/////////////////////////////////////////////
	//////////////////////////////////////////////
	app.Get("/", funciones.MostrarPaginaPrincipal)
	/////////////////////////////////////////////
	app.Get("/pedidos", funciones.MostrarPedidos)
	/////////////////////////////////////////////
	app.Get("/proyectos", funciones.MostrarProyectos)
	/////////////////////////////////////////////

	/////////////////////////////////////////////
	//               RUTAS:'POST'              //
	/////////////////////////////////////////////
	/////////////////////////////////////////////
	app.Post("/agregar", funciones.AgregarPedido)
	/////////////////////////////////////////////

}
